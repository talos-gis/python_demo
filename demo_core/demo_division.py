# Python has 3 division-related operations:

# /: also called "true division", this is mathematical division that returns a float.
print(2 / 3)
print(3 / 2)
print(-1.5 / 6)
print(10 / 2)

# //: also called "floor division", this rounds the result down to an integer
print(2 // 3)
print(3 // 2)
print(-10 // 3)

# %: the modulo operator, this is the remainder from integer division
print(2 % 3)
print(3 % 2)
print(-10 % 3)

# the following holds for integers: (x // y) * y + (x % y) == x
